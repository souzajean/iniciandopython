#Operadores de atribuição

#+= Soma com valor atribuído
a+=b # a=a+b
print('Soma com valor atribuído a =',a)

#-= Subtrai o valor atribuído
a-=b #a=a-b
print('Subtrai o valor atribuído a =',a)

#/= Divide pelo valor atribuído
a/=b
print('Divide pelo valor atribuído a =',a)

#*= Multiplica pelo valor atribuído
a*=b
print('Multiplica pelo valor atribuído a =',a)



