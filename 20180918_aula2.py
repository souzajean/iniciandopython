#Operadores Aritméticos

#variavel
a=10
b=2

#soma
resultadosoma=(a+b)
print('resultado soma a+b =',resultadosoma)

#divisao
resulatdodivisao=(a/b)
resultadodivisao1=(b/a)
print('resultado divisao a/b =',resulatdodivisao)
print('resultado divisao b/a =',resultadodivisao1)

#multiplicacao
resultadomultiplicacao=(a*b)
print('resultado multiplicacao a*b =',resultadomultiplicacao)

#exponenciacao
#2**3 = 8 # É o mesmo que dois ao cubo
#2**(3+6) = 512 # Dois elevado a nove
resultadoexponenciacao=(a**b)
print('resultado exponenciacao a**b =',resultadoexponenciacao)

#modulo

resultadomodulo=(a%b)
resultadomodulo1=(b%a)
print('O resto da divisão entre a%b =',resultadomodulo)
print('O resto da divisão entre b%a =',resultadomodulo1)


#Atribuindo um valor para varias variaveis
