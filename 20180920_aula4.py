#Operadores aritimeticos

#x + y (adição),
#x - y (subtração),
#x / y (divisão em ponto flutuante),
#x// y (divisão descartando parte fracionária),
#x * y , (multiplicação),
#x % y (resto),
#x (negação)
#x ** y (potência)


print(1+2)
print(3-1)
print(10/2)
print(10//3)
print(10*2+1)
print(10%3)
print(-3)
print(2**8)


#Operadores de bits

#x | y (ou),
#x ^y (ou exclusivo),
#x & y (e),
#x << y (x com y bits deslocados à esquerda),
#x >> y (x com y bits deslocados à direita)
#~x (inverso em bits).

print('Operadores de bits: 1 | 0 (ou)', 1 | 0)
print('Operadores de bits: 2 | 5 (ou)', 2 | 5)
print('Operadores de bits: 1 ^5 (ou exclusivo)',1^5)
print('Operadores de bits: 4 & 1 (e)',4&1)
print('Operadores de bits: x << y (x com y bits deslocados à esquerda)',1<<2)
print('Operadores de bits: 4 >> 2 (x com y bits deslocados à direita)',2>>4)
print('Operadores de bits: ~4 (inverso em bits)',~4)












